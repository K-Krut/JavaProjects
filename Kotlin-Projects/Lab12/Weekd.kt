/**
 * Enum клас Weekd - представляє дні тижня
 * Кожен день тижня асоційований з певним інтовим значенням, що відповідає його порядковому номеру у тижні
 */
enum class Weekd(val num: Int) {
    Monday(1),
    Tuesday(2),
    Wednesday(3),
    Thursday(4),
    Friday(5),
    Saturday(6),
    Sunday(7)
}
